            <li>OSS investor-level account confirmation</li>
            <li>Minimum investment fulfilment evidence (IDR 10 billion for PT PMA)</li>
            <li>Company bank account statement (capital injection proof)</li>
            <li>Sponsor guarantee letter from the PT PMA</li>
            <li>Director approval / board resolution for sponsorship</li>
          </ul>
        </div>

