      <div>
        <span class="eyebrow">Overview</span>
        <h2 class="section-title">What Is an Investor KITAS?</h2>
        <p>
          An <strong>Investor KITAS</strong> is a type of KITAS (Kartu Izin Tinggal Terbatas –
          Temporary Stay Permit) issued specifically to foreign nationals who hold a valid
          <strong>shareholder position in a PT&nbsp;PMA</strong> (foreign-owned limited liability
