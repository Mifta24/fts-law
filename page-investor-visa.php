        </p>
        <p>
          Unlike a working KITAS — which requires a Work Permit (IMTA) from the Ministry of
          Manpower — the Investor KITAS is issued based on your registered investment position
          through the Indonesian Investment Coordinating Board (BKPM) and the Online Single
          Submission (OSS) system.
        </p>
        <p>
          This makes the Investor KITAS one of the most practical residency options for
          foreign entrepreneurs and business owners who want to live in and actively manage
          their business in Indonesia.
        </p>

        <div class="info-box">
