            If your PT&nbsp;PMA is deregistered, becomes inactive, or your shareholder
            position is removed, your Investor KITAS will be affected and may be
            cancelled by immigration authorities. Keeping your company legally active
            is essential to maintaining your Investor KITAS validity.
          </p>
        </div>

        <div class="note-card note-card--info">
          <span class="note-card__icon" aria-hidden="true">📋</span>
          <h4>LKPM Reporting Required</h4>
          <p>
            PT&nbsp;PMA companies with active Investor KITAS holders are required to
            submit quarterly LKPM (Investment Activity Report) to BKPM/OSS. Failure to
            comply may result in penalties and affect the Investor KITAS renewal.
            Our office monitors and manages LKPM reporting as part of our ongoing
            legal support.
