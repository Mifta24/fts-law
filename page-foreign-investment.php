  <!-- INVESTMENT REGULATIONS -->
  <section class="section-soft">
    <div class="container">

      <h2 class="section-title">Key Investment Regulations for Foreign Investors</h2>
      <p class="section-subtitle">
        The following regulatory requirements apply to most foreign investors establishing
        or operating a business in Indonesia through a PT PMA structure.
      </p>

      <div class="grid-3">

        <article class="card">
          <div class="card-icon" aria-hidden="true">📋</div>
          <h3>OSS / NIB Registration</h3>
          <p>
            All businesses in Indonesia, including PT PMA companies, must be registered
            through the Online Single Submission (OSS) system to obtain a Business Identity
            Number (NIB — Nomor Induk Berusaha). The NIB is the primary business registration
            document and prerequisite for all further licences.
          </p>
        </article>

        <article class="card">
          <div class="card-icon" aria-hidden="true">📊</div>
          <h3>LKPM Investment Reporting</h3>
          <p>
            PT PMA companies are required to submit the <strong>LKPM</strong> (Laporan
            Kegiatan Penanaman Modal — Investment Activity Report) to BKPM/OSS on a
            quarterly and annual basis. Failure to submit results in administrative
            sanctions and may affect the company's investment registration status.
          </p>
        </article>

        <article class="card">
          <div class="card-icon" aria-hidden="true">💰</div>
          <h3>Minimum Investment Obligation</h3>
          <p>
            Foreign investment companies (PT PMA) must have a minimum investment plan of
            <strong>IDR 10 billion</strong> per business field (KBLI code) registered with
            OSS. Additionally, a minimum paid-up capital of <strong>IDR 2.5 billion</strong>
            must be injected and evidenced in the company's Indonesian bank account.
          </p>
        </article>

        <article class="card">
          <div class="card-icon" aria-hidden="true">🏛</div>
          <h3>BKPM Foreign Investment Notification</h3>
          <p>
            Certain investment activities and changes to the company (share transfers,
            capital increases, change of business activity) require formal notification
            or approval from BKPM. Our office monitors regulatory requirements and
            manages BKPM notifications on behalf of clients.
          </p>
        </article>

        <article class="card">
          <div class="card-icon" aria-hidden="true">💼</div>
          <h3>Foreign Worker Utilisation (RPTKA)</h3>
          <p>
            PT PMA companies that wish to employ foreign workers must obtain approval
            for a Foreign Worker Utilisation Plan (<strong>RPTKA</strong>) from the
            Ministry of Manpower. Each foreign worker must then obtain a Work Permit
            (IMTA) before beginning employment.
          </p>
        </article>

        <article class="card">
          <div class="card-icon" aria-hidden="true">🧾</div>
          <h3>Indonesian Tax Obligations</h3>
          <p>
            PT PMA companies are subject to Indonesian corporate income tax (25%),
            Value Added Tax (VAT — 11%), and withholding tax obligations. Annual tax
            returns must be filed with the Directorate General of Taxation. We advise
            on tax structure optimisation within the legal framework.
          </p>
        </article>

      </div>

    </div>
