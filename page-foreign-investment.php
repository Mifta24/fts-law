            <p>
              An annual investment activity report is required in addition to quarterly
              reports. This provides a full-year summary of investment, workforce, and
              operational data to BKPM.
            </p>
            <span class="compliance-item__deadline">Deadline: January of each year</span>
          </div>
        </div>

        <div class="compliance-item">
          <div class="compliance-item__icon" aria-hidden="true">🧾</div>
          <div class="compliance-item__content">
