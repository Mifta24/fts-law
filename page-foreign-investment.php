          <h4>Investment Protection</h4>
          <p>
            Foreign investors are entitled to legal protection against nationalisation
            and expropriation without fair compensation, consistent with international
            investment protection principles.
          </p>
        </div>

        <div class="rights-card">
          <span class="rights-card__icon" aria-hidden="true">💸</span>
          <h4>Repatriation of Funds</h4>
          <p>
            Foreign investors have the right to repatriate capital, profits, dividends,
            and other proceeds from their investment in Indonesia, subject to applicable
            tax obligations being fulfilled.
          </p>
        </div>

        <div class="rights-card">
          <span class="rights-card__icon" aria-hidden="true">⚖️</span>
          <h4>Equal Treatment</h4>
          <p>
            Foreign investors are entitled to equal treatment under Indonesian law as
            domestic investors, except where specific restrictions apply under the
            Positive Investment List or sectoral regulations.
          </p>
        </div>

        <div class="rights-card">
          <span class="rights-card__icon" aria-hidden="true">🤝</span>
          <h4>Dispute Resolution</h4>
          <p>
            Foreign investors may choose Indonesian courts, BANI arbitration, or
            international arbitration (e.g., SIAC, ICC) for resolving business
            disputes, subject to agreement in contracts.
          </p>
        </div>

      </div>

