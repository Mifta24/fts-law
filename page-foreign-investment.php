            <p>
              PT PMA companies must file an annual corporate income tax return (SPT Badan)
              with the Directorate General of Taxation (DJP). Monthly tax payments may
              also be required depending on revenue activity.
            </p>
            <span class="compliance-item__deadline">Deadline: April 30 each year</span>
          </div>
        </div>

        <div class="compliance-item">
          <div class="compliance-item__icon" aria-hidden="true">🏛</div>
          <div class="compliance-item__content">
            <h4>Annual GMS (General Meeting of Shareholders)</h4>
            <p>
              Indonesian company law requires PT PMA companies to hold an Annual General
              Meeting of Shareholders (RUPS Tahunan) to approve financial statements,
              ratify director actions, and make key corporate decisions.
            </p>
            <span class="compliance-item__deadline">Required annually within 6 months of fiscal year end</span>
          </div>
        </div>

        <div class="compliance-item">
          <div class="compliance-item__icon" aria-hidden="true">📋</div>
          <div class="compliance-item__content">
            <h4>NIB & Licence Renewal</h4>
            <p>
              Certain sector-specific business licences obtained through OSS may have
              validity periods and renewal requirements. Failure to renew on time may
              result in licence lapse and operational disruption.
            </p>
            <span class="compliance-item__deadline">Varies by licence type</span>
          </div>
        </div>

        <div class="compliance-item">
          <div class="compliance-item__icon" aria-hidden="true">🔄</div>
          <div class="compliance-item__content">
            <h4>Capital Realisation Reporting</h4>
            <p>
              The progress of capital injection (paid-up capital) must be reported to BKPM
              as part of the LKPM reporting. Failure to show progress on capital injection
              may result in queries from BKPM and suspension of investment registration.
            </p>
            <span class="compliance-item__deadline">Via quarterly LKPM reports</span>
          </div>
